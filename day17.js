// 判断两个数组是否相等
// 前提: 数组可能出现嵌套，每层数组的元素都是简单数据类型
// 判断相等条件: 数组元素顺序和每层元素的值是相同的
// a1 = [1]
// a2 = ['1']  // false

// a1 = [1, 2]
// a2 = [1, [2]]; // false

// a1 = [1, 2, 3, 4]
// a2 = [1, 2, 3, 4] // true

// a1 = ['a', ['b', ['c']], 'd']
// a2 = ['a', ['b', ['c']], 'd']  // true